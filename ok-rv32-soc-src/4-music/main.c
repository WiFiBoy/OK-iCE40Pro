void main()
{
    uint32_t count=0;
    
	lcd_init();                     // initialize LCD
	lcd_fillScreen(COLOR_BLACK);    // clear screen, check lcd.h for colors
	lcd_drawStr(24,30,"Melody Test V1", COLOR_GREEN, COLOR_BLACK);
	lcd_emptyRect(24, 70, 112, 24, COLOR_RED);
    lcd_drawStr(32,78,"OK-iCE40 Pro", COLOR_YELLOW, COLOR_BLACK);
	lcd_on();                       // turn on LCD backlight 
	
	while(1) {
	    count++;
	    if (count>41000) count=0;
	    if ((count % 100)==0) {
	        if (melody[count/100]>0)
	            spkr=freq[melody[count/100]+10];
	        
	    }
	    clkcnt_delayus(400);
	}
}
